            <b>Què són les galetes?</b><br>
            Les galetes (cookies en anglès) són petits arxius de text que s'instal·len als equips des dels quals s'accedeix al nostre lloc web. Poden guardar la identificació de l'usuari que visita el web i els llocs pels quals navega. Quan l'usuari (vostè en aquest cas) ens torna a visitar, es llegeixen les cookies per a identificar-lo i restablir les seves preferències o configuració de navegació. Si un usuari no autoritza l'ús de les galetes, alguns serveis o funcionalitats del lloc web podrien no estar disponibles.<br><br>
            <b>Què fan les galetes?</b><br>
            Les galetes no t'identifiquen personalment i no suposes cap risc per al teu ordinador o dispositiu mòbil. No són virus ni programari espia. Simplement, són eines utilitzades per als llocs web que visites amb l'objectiu de millorar la navegació.<br><br>
